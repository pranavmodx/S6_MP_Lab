ASSUME DS:DATA,CS:CODE

DATA SEGMENT
FIRST DB 0AH,0DH,"Enter 1st Number:$"
SECOND DB 0AH,0DH,"Enter 2nd Number:$"
RESULT DB 0AH,0DH,"The answer is:$"
DATA ENDS

CODE SEGMENT
start:
	MOV AX,DATA
	MOV DS,AX

	LEA DX,FIRST
	MOV AH,09H
	INT 21H

	MOV AH,01H;Read 1st digit of 1st no
	INT 21H
	MOV BH,AL
	MOV AH,01H;Read 2nd digit of 1st no
	INT 21H
	MOV BL,AL

	LEA DX,SECOND
	MOV AH,09H
	INT 21H

	MOV AH,01H;Read 1st digit of 2nd no
	INT 21H
	MOV CH,AL
	MOV AH,01H;Read 2nd digit of 2nd no
	INT 21H
	MOV CL,AL

	LEA DX,RESULT
	MOV AH,09H
	INT 21H

	MOV AH,00H;Clear AH
	SUB BL,CL
	MOV AL,BL
	AAS
	ADD AL,30H
	MOV CL,AH  ;Borrow OF LOWER DIGIT
	MOV BL,AL  ;ANSWER PART 1

	MOV AH,00H;Clear AH
	SUB BH,CH
	MOV AL,BH
	AAS
	ADD AH,30H
	MOV BH,AH  ;ANSWER PART 3
	ADD AL,CL
	ADD AL,30H
	MOV CH,AL  ;ANSWER PART 2


	MOV DL,CH
	MOV AH,02H
	INT 21H

	MOV DL,BL
	MOV AH,02H
	INT 21H

	MOV AH,4CH
	INT 21H

CODE ENDS
END START
